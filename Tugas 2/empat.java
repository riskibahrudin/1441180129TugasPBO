class empat{
	public static void main(String[] args) {
		int angka = 1;

		switch (angka) {
			
			case 1:
			System.out.print("Satu"); break;
			case 2:
			System.out.print("Dua"); break;
			case 3:
			System.out.print("Tiga"); break;
			case 4:
			System.out.print("Empat"); break;
			case 5:
			System.out.print("Lima"); break;
			case 6:
			System.out.print("Enam"); break;
			case 7:
			System.out.print("Tujuh"); break;
			case 8:
			System.out.print("Delapan"); break;
			case 9:
			System.out.print("Sembilan"); break;
			default:
				System.out.print("Tidak ada");

		}
	}
}